class Switchex{
	public static void main(String[]args){
		char ch='A';

		switch(ch){
			case 'A':
				System.out.println('A');
				break;
			case 'B':
				System.out.println('B');
				break;
			case 67:
				System.out.println(67);
				break;
			default:
				System.out.println("default");
				break;

		}
	}
}
